<h1><center>The Unix Tree</center></h1>

Welcome to the Unix Tree. Here you can browse the source code and manuals
of various old versions of Unix. For every file, you can also find related
files from other versions: this can help show how the different versions of
Unix are related. Most of the Unix versions below come from the
<a href="http://wiki.tuhs.org/doku.php?id=source:unix_archive">Unix Archive</a>.
<p>
<i>If you want to get a copy of all the files in the Unix Tree, please don't
spider this website as it causes a high load. E-mail me for a copy. -- 
whiskey kilo tango at TUHS dot ORG.</i>
